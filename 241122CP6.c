#include<stdio.h>
int main()                          
{
    int N1, N2 , temp;
    N1=20;
    N2=30;
    printf("%d\t%d\n ",N1,N2);
    temp=N1;
    N1=N2;
    N2=temp;
    printf("%d\t%d",N1,N2);
}